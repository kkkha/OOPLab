package convert;
public class Currency
{
    public static double dtoi(double d)
    {
	    return 68.51*d;
    }
    public static double itod(double d)
    {
	    return d/68.51;
    }
        public static double etoi(double d)
    {
	    return 80.09*d;
    }
    public static double itoe(double d)
    {
	    return d/80.09;
    }
    public static double ytoi(double d)
    {
	    return d*0.61;
    }
    public static double itoy(double d)
    {
	    return d/0.61;
    }
}
