package convert;
public class Distance
{
    public static double kmtomi(double d)
    {
	    return d/1.6;
    }
    public static double mitokm(double d)
    {
	    return d*1.6;
    }
    public static double mtokm(double d)
    {
	    return d/1000.0;
    }
    public static double kmtom(double d)
    {
	    return d*1000.0;
    }
}
